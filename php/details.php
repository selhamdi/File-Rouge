  <div class="pie-recipe">
    <div class="pie-recipe__title">
      <div class="pie-name">
       Panna cotta</div>
      <div class="pie-serving">
        <p>Makes one 9½-inch pie</p>
      </div>
    </div>
    <div class="pie-recipe__ingredients">
      <div class="pie-recipe__ingredients__item">
        <h4>sugar cone pie shell</h4>
        <ul>
          <li>1¾ cups ice cream sugar cone crumbs</li>
          <li>¾ stick unsalted butter, melted</li>
        </ul>
        <h4>filling</h4>
        <ul>
          <li>½ cup sliced almonds</li>
          <li>2 pt. mocha almond ice cream, toasted and cooled</li>
        </ul>
      </div>
      <div class="pie-recipe__ingredients__item">
        <h4>glaze</h4>
        <ul>
          <li>6 oz. semisweet chocolate, chopped</li>
          <li>1 cup confectioners’ sugar</li>
          <li>½ stick unsalted butter</li>
          <li>¼ cup heavy cream</li>
          <li>2 tbsp. light corn syrup</li>
          <li>1 tsp. almond extract</li>
        </ul>
      </div>
    </div>
    <div class="pie-recipe__subtitle">instructions</div>
    <div class="pie-recipe__number">1</div>
    <div class="pie-recipe__steps">For the pie shell: Preheat the oven to 350°F. Toss together the crumbs and butter in a medium bowl to moisten. Press the mixture onto the bottom and up the sides of a 9½-inch deep-dish pie pan.</div>
    <div class="pie-recipe__number">2</div>
    <div class="pie-recipe__steps">Refrigerate for at least 15 minutes to firm up the butter. Bake in the center of the oven for about 10 minutes, or until just set and fragrant. Place on a wire rack to cool to room temperature.</div>
    <div class="pie-recipe__number">3</div>
    <div class="pie-recipe__steps">For the glaze: Heat the chocolate, confectioners’ sugar, butter, cream, and corn syrup in a small saucepan over low heat, stirring until smooth. Remove the pan from the heat and stir in the almond extract. Let sit until lukewarm.</div>
    <div class="pie-recipe__number">4</div>
    <div class="pie-recipe__steps">Pour half the glaze into the bottom of the pie shell and spread until even; refrigerate for 30 minutes, or until the glaze sets. Reserve the rest of the glaze.</div>
    <div class="pie-recipe__number">5</div>
    <div class="pie-recipe__steps">For the filling: Let the ice cream sit at room temperature for about 10 minutes, or until soft enough to scoop and spread. Spread 2 cups of the ice cream in the pie shell.</div>
    <div class="pie-recipe__number">6</div>
    <div class="pie-recipe__steps">If the reserved glaze has turned too firm to pour, reheat it over very low heat. Drizzle the ice cream with half the remaining glaze and sprinkle with half the almonds.</div>
    <div class="pie-recipe__number">7</div>
    <div class="pie-recipe__steps">Return to the refrigerator to chill for 15 minutes, then repeat the layers. Freeze for 2 hours, or until firm.</div>
